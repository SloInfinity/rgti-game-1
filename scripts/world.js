World.addToScene = function () {
	var floorLoader = new THREE.TextureLoader();
	floorLoader.load(
		"./assets/dungeon-floor-texture.png" ,
		function (texture) {
			texture.wrapS = texture.wrapT = THREE.RepeatWrapping; 
			texture.repeat.set( 25, 25 );
			var floorMaterial = new THREE.MeshLambertMaterial( {map: texture, side: THREE.DoubleSide} );
			var floorGeometry = new THREE.PlaneGeometry(50, 50, 2, 2);
			var floor = new THREE.Mesh(floorGeometry, floorMaterial);
			floor.position.y = 0;
			floor.rotation.x = Math.PI / 2;
			scene.add(floor);
		}
	);


	var wallLoader = new THREE.TextureLoader();
	wallLoader.load(
		"./assets/dungeon-wall-texture.jpg" ,
		function (texture) {
			texture.wrapS = texture.wrapT = THREE.RepeatWrapping; 
			texture.repeat.set( 1, 3 );

			var wallMaterial = new THREE.MeshLambertMaterial( {map: texture, side: THREE.FrontSide} );
			var wallGeometry = new THREE.BoxGeometry(1,3,1);

			for(var z = 0; z < World.maze.length; z++){
				for(var x = 0; x < World.maze[z].length; x++){
					if(World.maze[z][x] == 1){

						var wall = new THREE.Mesh(wallGeometry, wallMaterial);

						wall.position.y = 1.5;

						wall.position.z = z-World.offsetZ;
						wall.position.x = x-World.offsetX;
						scene.add(wall);
						collidableMeshList.push(wall);

					}
				}
			}
		}
	);
};